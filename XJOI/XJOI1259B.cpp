inline int Mul(int a, int b) { return 1ll * a * b % kMod; }
int FPow(int bs, int ex = kMod - 2) {
	int res = 1;
	for (; ex; bs = Mul(bs, bs), ex >>= 1)
		if (ex & 1) res = Mul(res, bs);
	return res;
}

const int kMaxn = 5005;

int n;
int sum[kMaxn];
double DP[kMaxn], F[kMaxn], G[kMaxn];

int main() {
	scanf("%d", &n);
	for (int i = 1; i <= n; ++i) {
		scanf("%d", sum + i);
		sum[i] += sum[i - 1];
	}
	for (int i = 2; i <= n; ++i) {
		for (int l = 1; l + i - 1 <= n; ++l) {
			DP[l] = 1.0 * (F[l] + G[l + i - 1]) / (i - 1)  +
				sum[l + i - 1] - sum[l - 1];
		}
		for (int l = 1; l <= n; ++l) {
			F[l] += DP[l];
			if (l + i - 1 <= n)
				G[l + i - 1] += DP[l];
		}
	} printf("%.5lf\n", DP[1]);
	return 0;
}
