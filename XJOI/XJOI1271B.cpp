int main() {
	scanf("%d", &n);
	if (n > 2) {
		int i = 0, cnt = 0;
		for (; kTab[i].fi < n; ++i);
		for (; kTab[i].fi <= n; ++i, ++cnt)
			printf("%lld ", kTab[i].se);
		if (cnt == 0) puts("-1");
		else putchar('\n');
	}
	if (n == 1) {
		for (int i = 1; i <= 9; ++i) {
			putchar('0' ^ i);
			putchar(' ');
		} putchar('\n');
	}
	if (n == 2) {
		int cnt = 0;
		for (int i = 10; i < 100; ++i) {
			int b1 = i % 10, b2 = i / 10;
			if (b1 * b1 + b2 * b2 == i) {
				printf("%d ", i);
				++cnt;
			}
		} if (!cnt) puts("-1");
		else putchar('\n');
	} return 0;
}
